def run():
    db = SessionLocal()
    try:
        # Создаём категории
        category_map = {}
        for title in CATEGORIES:
            cat = db.query(KnowledgeCategory).filter_by(title=title).first()
            if not cat:
                cat = KnowledgeCategory(title=title)
                db.add(cat)
                db.commit()
                db.refresh(cat)
            category_map[title] = cat

        # Создаём статьи
        for art in ARTICLES:
            exists = db.query(KnowledgeArticle).filter_by(title=art["title"]).first()
            if not exists:
                new_article = KnowledgeArticle(
                    title=art["title"],
                    content=art["content"],
                    category_id=category_map[art["category"]].id,
                )
                db.add(new_article)
                db.commit()

        print("✅ База знаний успешно заполнена 15 статьями")
    finally:
        db.close()


if __name__ == "__main__":
    run()
